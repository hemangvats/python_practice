# Hemang Vats, XI-A, displaying average and percentage of marks
marks1=int(input("Enter marks in first subject:"))
marks2=int(input("Enter marks in second subject:"))
marks3=int(input("Enter marks in third subject:"))
average=(marks1+marks2+marks3)/3
print("The average marks are:",average)
percentage=((marks1+marks2+marks3)/240)*100
print("The percentage of marks is:",percentage)
